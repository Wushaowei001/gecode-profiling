// Instance data
namespace {

  // Instances
  extern const int* qcp[];
  // Instance names
  extern const char* name[];

  /// A wrapper class for instance data
  class Spec {
  protected:
    /// Raw instance data
    const int* data;
    /// Access info at position \a i and \a j
    int info(int i, int j) const {
      int n = data[0];
      assert((i >= 0) && (i < n));
      assert((j >= 0) && (j < n));
      return data[1 + (i * n) + j];
    }
    /// Find instance by name \a s
    static const int* find(const char* s) {
      for (int i=0; name[i] != NULL; i++)
        if (!strcmp(s,name[i]))
          return qcp[i];
      return NULL;
    }
  public:
    /// Whether a valid specification has been found
    bool valid(void) const {
      return data != NULL;
    }
    /// Initialize
    Spec(const char* s) : data(find(s)) {}
    /// Return problem size
    int size(void) const {
      return data[0];
    }
    /// Whether field is pre-assigned
    bool assigned(int i, int j) const {
      return info(i,j) > 0;
    }
    /// Return value at position \a i and \j
    int val(int i, int j) const {
      assert(assigned(i,j));
      return info(i,j) - 1;
    }
  };

}


/**
 * \brief %Example: Quasigroup completion
 *
 * \ingroup Example
 *
 */
class QCP : public Script {
protected:
  /// Specification
  const Spec spec;
  /// Field elements e
  IntVarArray e;
public:
  /// Branching to use for model
  enum {
    BRANCH_SIZE,     ///< Use smallest domain size first
    BRANCH_AFC_SIZE, ///< Use largest AFC divided by domain size
  };
  /// Actual model
  QCP(const InstanceOptions& opt) 
    : spec(opt.instance()),
      e(*this, spec.size() * spec.size(), 0, spec.size()-1) {
    // Problem size
    int n = spec.size();
    // Matrix for elements
    Matrix<IntVarArray> m(e, n);

    // Assign fields
    for (int i=0; i<n; i++)
      for (int j=0; j<n; j++)
        if (spec.assigned(i,j))
          rel(*this, m(i,j) == spec.val(i,j));

    // Post constraints
    for (int i=0; i<n; i++) {
      distinct(*this, m.row(i), opt.icl());
      distinct(*this, m.col(i), opt.icl());
    }

    switch (opt.branching()) {
    case BRANCH_SIZE:
      branch(*this, e, INT_VAR_SIZE_MIN(), INT_VAL_MIN());
      break;
    case BRANCH_AFC_SIZE:
      branch(*this, e, INT_VAR_AFC_SIZE_MAX(), INT_VAL_MIN());
      break;
    }
  }
  /// Constructor for cloning \a s
  QCP(bool share, QCP& s) 
    : Script(share,s), spec(s.spec) {
    e.update(*this, share, s.e);
  }
  /// Copy during cloning
  virtual Space*
  copy(bool share) {
    return new QCP(share,*this);
  }
  /// Print solution
  virtual void
  print(std::ostream& os) const {
    int n = spec.size();
    Matrix<IntVarArray> m(e, n);

    for (int i=0; i<n; i++) {
      os << "\t";
      for (int j=0; j<n; j++) {
        if (m(i,j).assigned())
          os.width(2);
        os << m(i,j) << " ";
      }
      os << std::endl;
    }
  }
};

/** \brief Main-function
 *  \relates QCP
 */
int
main(int argc, char* argv[]) {
  InstanceOptions opt("QCP");
  opt.branching(QCP::BRANCH_AFC_SIZE);
  opt.branching(QCP::BRANCH_SIZE, "size");
  opt.branching(QCP::BRANCH_AFC_SIZE, "afc");
  opt.instance(name[0]);
  opt.parse(argc,argv);
  if (!Spec(opt.instance()).valid()) {
    std::cerr << "Error: unkown instance" << std::endl;
    return 1;
  }
  Script::run<QCP,DFS,InstanceOptions>(opt);
  return 0;
}

